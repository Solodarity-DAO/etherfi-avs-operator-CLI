var arpaAPI *arpa.API
var etherfiAPI *etherfi.API

var ARPACmd = &cli.Command{
	Name:   "arpa",
	Usage:  "various actions related to managing ARPA operators",
	Before: prepareCmd,
	Commands: []*cli.Command{
		RegisterCmd,
		GenerationRegisterSignatureCmd,
	},
}

// run before any subcommand executes
func prepareCmd(ctx context.Context, cmd *cli.Command) error {
	// try to load RPC_URL from env or flags
	rpcURL := os.Getenv("RPC_URL")
	if cmd.String("rpc-url") != "" {
		rpcURL = cmd.String("rpc-url")
	}
	if rpcURL == "" {
		return fmt.Errorf("must set env var $RPC_URL or use --rpc-url flag")
	}
	rpcClient, err := ethclient.Dial(rpcURL)
	if err != nil {
		return fmt.Errorf("dialing RPC: %w", err)
	}

	// load all required addresses for this chain and bind applicable contracts
	cfg, err := config.AutodetectConfig(rpcClient)
	if err != nil {
		return fmt.Errorf("loading config: %w", err)
	}

	// make globally accessible by all sub commands
	etherfiAPI = etherfi.New(cfg, rpcClient)
	arpaAPI = arpa.New(cfg, rpcClient)

	return nil
}

var RegisterCmd = &cli.Command{
	Name:   "register-node",
	Usage:  "(Node Operator) register a node with the ARPA service",
	Action: handleRegistration,
	Flags: []cli.Flag{
		&cli.IntFlag{
			Name:     "operator-id",
			Usage:    "Operator ID",
			Required: true,
		},
		&cli.StringFlag{
			Name:     "dkg-public-key",
			Usage:    "Uncompressed BLS public key generated by the ARPA node",
			Required: true,
		},
		&cli.StringFlag{
			Name:     "registration-signature",
			Usage:    "path to registration signature file created by prepare-registration command",
			Required: true,
		},
	},
}

func handleRegistration(ctx context.Context, cli *cli.Command) error {

	// parse cli input
	operatorID := cli.Int("operator-id")
	dkgPublicKeyHex := cli.String("dkg-public-key")
	registrationInputPath := cli.String("registration-signature")

	// look up operator contract associated with this id
	operator, err := etherfiAPI.LookupOperatorByID(operatorID)
	if err != nil {
		return fmt.Errorf("looking up operator address: %w", err)
	}
	// Convert hex string back to bytes
	dkgPublicKey, err := hex.DecodeString(dkgPublicKeyHex)
	if err != nil {
		return fmt.Errorf("invalid dkg-public-key: %v", err)
	}
	// read input file with provided registration signature
	var inputSignature arpa.ISignatureUtilsSignatureWithSaltAndExpiry
	buf, err := os.ReadFile(registrationInputPath)
	if err != nil {
		return fmt.Errorf("reading input file: %w", err)
	}
	if err := json.Unmarshal(buf, &inputSignature); err != nil {
		return fmt.Errorf("parsing registration signature: %w", err)
	}
	if len(inputSignature.Signature) == 0 {
		return fmt.Errorf("invalid registration , missing signature")
	}
	if bytes.Equal(inputSignature.Salt[:], make([]byte, 32)) {
		return fmt.Errorf("invalid registration input, missing salt")
	}
	if inputSignature.Expiry == nil {
		return fmt.Errorf("invalid registration input, missing expiry")
	}

	return arpaAPI.Register(operator, dkgPublicKey, inputSignature)
}

var GenerationRegisterSignatureCmd = &cli.Command{
	Name:   "generate-registration-signature",
	Usage:  "(Admin) generate a registration signature for the given operator",
	Action: handleGenerateRegistrationSignature,
	Flags: []cli.Flag{
		&cli.IntFlag{
			Name:     "operator-id",
			Usage:    "Operator ID",
			Required: true,
		},
	},
}

func handleGenerateRegistrationSignature(ctx context.Context, cli *cli.Command) error {

	// parse cli input
	operatorID := cli.Int("operator-id")

	// look up operator contract associated with this id
	operator, err := etherfiAPI.LookupOperatorByID(operatorID)
	if err != nil {
		return fmt.Errorf("looking up operator address: %w", err)
	}

	// load eip-1271 admin signing key
	signingKey, err := crypto.HexToECDSA(os.Getenv("ADMIN_1271_SIGNING_KEY"))
	if err != nil {
		return fmt.Errorf("invalid private key: %w", err)
	}

	return arpaAPI.GenerateAVSRegistrationSignature(operator, signingKey)
}
